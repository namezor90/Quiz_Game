class Question:
    """
    Egy kvíz kérdést reprezentáló osztály.

    Attribútumok:
        text (str): A kérdés szövege
        answer (str): A kérdés helyes válasza
    """
    def __init__(self, text, answer):
        """
        Question példány inicializálása.

        Args:
            text (str): A kérdés szövege
            answer (str): A helyes válasz
        """
        self.text = text
        self.answer = answer
        self.text = text
        self.answer = answer
